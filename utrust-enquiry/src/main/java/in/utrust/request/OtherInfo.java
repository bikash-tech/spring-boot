package in.utrust.request;

public class OtherInfo {
	
	private Boolean  originalInvoice;
	private int serviceHistoryId;
	private Boolean taxToPrivate;
	private int noOfSpareKeys;
	public Boolean getOriginalInvoice() {
		return originalInvoice;
	}
	public void setOriginalInvoice(Boolean originalInvoice) {
		this.originalInvoice = originalInvoice;
	}
	public int getServiceHistoryId() {
		return serviceHistoryId;
	}
	public void setServiceHistoryId(int serviceHistoryId) {
		this.serviceHistoryId = serviceHistoryId;
	}
	public Boolean getTaxToPrivate() {
		return taxToPrivate;
	}
	public void setTaxToPrivate(Boolean taxToPrivate) {
		this.taxToPrivate = taxToPrivate;
	}
	public int getNoOfSpareKeys() {
		return noOfSpareKeys;
	}
	public void setNoOfSpareKeys(int noOfSpareKeys) {
		this.noOfSpareKeys = noOfSpareKeys;
	}
	


}
